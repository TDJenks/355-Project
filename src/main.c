void myADC_Init()
{
	/* Enable clock for ADC */
	RCC->APB2ENR |= RCC_APB2ENR_ADCEN;

	/* Calibration */
	if (ADC1->CR & ADC_CR_ADEN)
		ADC1->CR |= ADC_CR_ADDIS;

	ADC1->CR |= ADC_CR_ADCAL;
	while (ADC1->CR & ADC_CR_ADCAL);

	/* Continuous conversion mode */
	ADC1->CFGR1 |= ADC_CFGR1_CONT;

	/* Channel 1 */
	ADC1->CHSELR = ADC_CHSELR_CHSEL1;

	/* Sampling time */
	ADC1->SMPR |= ADC_SMPR_SMP_2;


	/* Enable ADC */
	ADC1->CR |= ADC_CR_ADEN;
	while(!(ADC1->ISR & ADC_ISR_ADRDY));

	/* Start continuous conversion */
	ADC1->CR |=ADC_CR_ADSTART;

}

uint16_t adc_read(void)
{
		return ADC1->DR;
}

void myDAC_Init() {
	/* Enable clock for DAC */
	RCC->APB1ENR |= RCC_APB1ENR_DACEN;

	/* Enable DAC channel 1, buffer on */
	DAC->CR &= ~DAC_CR_BOFF1;
	DAC->CR |= DAC_CR_EN1;


}

void dac_write(uint16_t value)
{
	/* Write a DAC value (0-4095) */
	DAC->DHR12R1 = value;
}

void myGPIOA_Init()
{
	/* Enable clock for GPIOA peripheral */
	RCC->AHBENR |= RCC_AHBENR_GPIOAEN;

	/* Configure PA0 as input */
	GPIOA->MODER &= ~(GPIO_MODER_MODER0);

	/* Configure PA4 as analog */
	GPIOA->MODER |= 0x300;

	/* Configure PA1 as analog */
	GPIOA->MODER |= 0xC;

	/* Set PA0 to pull-down*/
	GPIOA->PUPDR |= GPIO_PUPDR_PUPDR0;
}

void myGPIOB_Init()
{
	/* Enable clock for GPIOB peripheral */
	RCC->AHBENR |= RCC_AHBENR_GPIOBEN;

	/* Configure PB2 as input */
	GPIOB->MODER &= ~(GPIO_MODER_MODER2);

	/* Configure PB3 as input */
	GPIOB->MODER &= ~(GPIO_MODER_MODER3);

	/* Configure PB8, PB9 and PB11 as output */
	GPIOB->MODER |= (GPIO_MODER_MODER8 | GPIO_MODER_MODER9 | GPIO_MODER_MODER11);

	/* Configure PB13 and PB15 as alternate function */
	GPIOB->MODER |= 0x80000000 | 0x08000000;

	/* Ensure no pull-up/pull-down for PB2 */
	GPIOB->PUPDR &= ~(GPIO_PUPDR_PUPDR2);

	/* Ensure no pull-up/pull-down for PB3 */
	GPIOB->PUPDR &= ~(GPIO_PUPDR_PUPDR3);
}

